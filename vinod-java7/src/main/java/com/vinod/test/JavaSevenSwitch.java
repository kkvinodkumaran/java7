package com.vinod.test;

public class JavaSevenSwitch {
	public static void main(String[] args) {
		String s = "java";
		// switch allows string from java7
		switch (s) {
		case "java":
			System.out.println("This is java");
			break;
		case ".net":
			System.out.println("This is ,net");
			break;
		}
	}
}